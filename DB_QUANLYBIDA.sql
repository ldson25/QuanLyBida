CREATE PROCEDURE [dbo].[USP_MERGETABLES]
@IDTABLE1 INT, @IDTABLE2 INT
AS 
BEGIN
    -- Khai báo biến
    DECLARE @IDFIRSTBILL INT
    DECLARE @IDSECONDBILL INT 
    DECLARE @ISFIRSTTABLE_EMPTY INT = 1
    DECLARE @ISSECONDTABLE_EMPTY INT = 1

    -- Kiểm tra nếu bàn thứ nhất trống
    IF NOT EXISTS (SELECT 1 FROM dbo.BILL WHERE IDTABLE = @IDTABLE1 AND STATUS = 0)
    BEGIN
        INSERT INTO BILL(DATECHECKIN, DATECHECKOUT, IDTABLE, STATUS)
        VALUES(GETDATE(), NULL, @IDTABLE1, 0)
    END

    -- Kiểm tra nếu bàn thứ hai trống
    IF NOT EXISTS (SELECT 1 FROM dbo.BILL WHERE IDTABLE = @IDTABLE2 AND STATUS = 0)
    BEGIN
        INSERT INTO BILL(DATECHECKIN, DATECHECKOUT, IDTABLE, STATUS)
        VALUES(GETDATE(), NULL, @IDTABLE2, 0)
    END

    -- Lấy ID của hóa đơn cho từng bàn
    SELECT @IDFIRSTBILL = IDBILL FROM dbo.BILL WHERE IDTABLE = @IDTABLE1 AND STATUS = 0
    SELECT @IDSECONDBILL = IDBILL FROM dbo.BILL WHERE IDTABLE = @IDTABLE2 AND STATUS = 0

    -- Kiểm tra xem bàn đầu tiên có dữ liệu không
    SELECT @ISFIRSTTABLE_EMPTY = CASE WHEN EXISTS (SELECT 1 FROM BILLINFO WHERE IDBILL = @IDFIRSTBILL) THEN 0 ELSE 1 END

    -- Kiểm tra xem bàn thứ hai có dữ liệu không
    SELECT @ISSECONDTABLE_EMPTY = CASE WHEN EXISTS (SELECT 1 FROM BILLINFO WHERE IDBILL = @IDSECONDBILL) THEN 0 ELSE 1 END

    -- Thực hiện MERGE
    MERGE INTO BILLINFO AS Target
    USING (
        SELECT @IDSECONDBILL AS IDBILL, IDFOOD, SUM(COUNT) AS COUNT
        FROM BILLINFO
        WHERE IDBILL = @IDFIRSTBILL
        GROUP BY IDFOOD
    ) AS Source ON Target.IDBILL = Source.IDBILL AND Target.IDFOOD = Source.IDFOOD
    WHEN MATCHED THEN 
        UPDATE SET Target.COUNT = Target.COUNT + Source.COUNT
    WHEN NOT MATCHED BY TARGET THEN 
        INSERT (IDBILL, IDFOOD, COUNT) VALUES (Source.IDBILL, Source.IDFOOD, Source.COUNT);

    -- Xóa dữ liệu từ bàn thứ nhất
    DELETE FROM BILLINFO WHERE IDBILL = @IDFIRSTBILL;

    -- Cập nhật trạng thái của bàn
	-- Cập nhật trạng thái của bàn dựa trên trạng thái sau khi gộp
	IF (@ISSECONDTABLE_EMPTY = 0)
		UPDATE TABLEFOOD SET STATUS = N'Trống' WHERE IDTABLE = @IDTABLE1;
	ELSE
		UPDATE TABLEFOOD SET STATUS = N'Có người' WHERE IDTABLE = @IDTABLE1;

END
GO
