CREATE OR ALTER PROCEDURE [dbo].[USP_InsertImportBill]
    @TotalPrice DECIMAL(18,2),
    @Note NVARCHAR(MAX)
AS
BEGIN
    INSERT INTO IMPORT_BILL (DATE_IMPORT, TOTAL_PRICE, NOTE)
    VALUES (GETDATE(), @TotalPrice, @Note);

    SELECT SCOPE_IDENTITY();
END
GO

-- 5. SP: Thêm chi tiết nhập & Tự động cộng tồn kho
CREATE OR ALTER PROCEDURE [dbo].[USP_InsertImportInfo]
    @IdBill INT,
    @IdFood INT,
    @Quantity INT,
    @InputPrice DECIMAL(18,2)
AS
BEGIN
    INSERT INTO IMPORT_INFO (ID_BILL, ID_FOOD, QUANTITY, INPUT_PRICE)
    VALUES (@IdBill, @IdFood, @Quantity, @InputPrice);

    UPDATE dbo.FOOD 
    SET INVENTORY = INVENTORY + @Quantity 
    WHERE IDFOOD = @IdFood;
END
GO

