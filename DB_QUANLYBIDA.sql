CREATE PROCEDURE [dbo].[MarkAttendance] 
    @EmployeeID INT,
    @WorkDate DATETIME,
    @WorkShift NVARCHAR(255)
AS
BEGIN
    DECLARE @ScheduleID INT;
    DECLARE @AttendanceID INT;
    
    -- Lấy ID của ca làm việc từ bảng EMPLOYEES
    SELECT @ScheduleID = SCHEDULEID FROM EMPLOYEES WHERE EMPLOYEEID = @EmployeeID;
    
    -- Kiểm tra xem nhân viên đã chấm công chưa
    SELECT @AttendanceID = ATTENDANCEID FROM ATTENDANCES WHERE EMPLOYEEID = @EmployeeID AND WORKDATE = @WorkDate;
    
    IF @AttendanceID IS NULL
    BEGIN
        -- Chưa chấm công, thêm mới vào bảng ATTENDANCES
        INSERT INTO ATTENDANCES (EMPLOYEEID, WORKDATE, WORKSHIFT, SCHEDULEID) VALUES (@EmployeeID, @WorkDate, @WorkShift, @ScheduleID);
    END
    ELSE
    BEGIN
        -- Đã chấm công, cập nhật lại ca làm việc
        UPDATE ATTENDANCES SET WORKSHIFT = @WorkShift WHERE EMPLOYEEID = @EmployeeID AND WORKDATE = @WorkDate;
    END
END;
GO
