CREATE PROCEDURE [dbo].[usp_INSERTBILLINFO]
    @IDBILL INT,
    @IDFOOD INT,
    @COUNT INT
AS 
BEGIN
    DECLARE @IsExitsBillInfo INT
    DECLARE @FOODCOUNT INT = 0

    SELECT @IsExitsBillInfo = ID, @FOODCOUNT = COUNT 
    FROM dbo.BILLINFO 
    WHERE IDBILL = @IDBILL AND IDFOOD = @IDFOOD

    IF(@IsExitsBillInfo IS NOT NULL) -- Nếu món ăn đã tồn tại trong hóa đơn
    BEGIN
        DECLARE @NewCount INT = @FOODCOUNT + @COUNT
        IF (@NewCount > 0) -- Nếu số lượng mới lớn hơn 0
        BEGIN
            UPDATE dbo.BILLINFO SET COUNT = @NewCount WHERE ID = @IsExitsBillInfo
        END
        ELSE -- Nếu số lượng mới bằng hoặc nhỏ hơn 0
        BEGIN
            DELETE dbo.BILLINFO WHERE ID = @IsExitsBillInfo
        END
    END
    ELSE -- Nếu món ăn chưa tồn tại trong hóa đơn
    BEGIN
        INSERT INTO dbo.BILLINFO(IDBILL, IDFOOD, COUNT)
        VALUES	(@IDBILL, @IDFOOD, @COUNT)
    END
END
