    @ID INT,
    @Name NVARCHAR(200),
    @Type INT,
    @Value FLOAT,
    @ApplyFoodID INT = NULL,
    @ReqQty INT = 0
AS
BEGIN
    UPDATE PROMOTION
    SET NAME = @Name,
        TYPE = @Type,
        VALUE = @Value,
        APPLY_FOR_FOOD_ID = @ApplyFoodID,
        REQUIRED_QTY = @ReqQty
    WHERE ID = @ID
END
GO

CREATE OR ALTER PROCEDURE [dbo].[USP_DeletePromotion]
    @ID INT
AS
BEGIN
    UPDATE PROMOTION SET STATUS = 0 WHERE ID = @ID
END
GO
