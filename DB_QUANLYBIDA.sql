CREATE OR ALTER PROCEDURE [dbo].[USP_SmartImportFood]
    @ImportBillID INT,
    @FoodID INT,            
    @FoodName NVARCHAR(100), 
    @CategoryID INT,
    @Price FLOAT,
    @Quantity INT,
    @Unit NVARCHAR(50),
    @Image NVARCHAR(MAX)
AS
BEGIN
    DECLARE @NewFoodID INT = @FoodID;

    IF EXISTS (SELECT * FROM dbo.FOOD WHERE IDFOOD = @FoodID)
    BEGIN
        UPDATE dbo.FOOD
        SET INVENTORY = INVENTORY + @Quantity,
            PRICE = @Price,
            UNIT = @Unit, 
            IDCATEGORY = @CategoryID
        WHERE IDFOOD = @FoodID;
    END
    ELSE
    BEGIN
        INSERT INTO dbo.FOOD (NAME, IDCATEGORY, PRICE, IMAGES, INVENTORY, UNIT)
        VALUES (@FoodName, @CategoryID, @Price, @Image, @Quantity, @Unit);
        
        SET @NewFoodID = SCOPE_IDENTITY();
    END

    INSERT INTO dbo.IMPORT_INFO (ID_BILL, ID_FOOD, QUANTITY, INPUT_PRICE)
    VALUES (@ImportBillID, @NewFoodID, @Quantity, @Price);
END
GO

CREATE OR ALTER PROCEDURE [dbo].[USP_GetInventoryList]
AS
BEGIN
    SELECT 
        F.IDFOOD, 
        F.NAME, 
        FC.NAME AS CategoryName, 
        F.UNIT, 
        F.PRICE, 
        F.INVENTORY 
    FROM dbo.FOOD F
    INNER JOIN dbo.FOODCATEGORY FC ON F.IDCATEGORY = FC.IDCATEGORY
    ORDER BY F.INVENTORY ASC 
END
GO

CREATE OR ALTER TRIGGER [dbo].[TRG_DeductStock_AfterCheckout]
ON [dbo].[BILL]
AFTER UPDATE
AS
BEGIN
    IF UPDATE(STATUS)
    BEGIN
        DECLARE @BillID INT
        DECLARE @NewStatus INT
        DECLARE @OldStatus INT

        SELECT @BillID = IDBILL, @NewStatus = STATUS FROM INSERTED
        SELECT @OldStatus = STATUS FROM DELETED

        IF (@OldStatus = 0 AND @NewStatus = 1)
        BEGIN
            UPDATE dbo.FOOD
            SET INVENTORY = INVENTORY - BI.COUNT
            FROM dbo.FOOD F
            INNER JOIN dbo.BILLINFO BI ON F.IDFOOD = BI.IDFOOD
            WHERE BI.IDBILL = @BillID
        END
    END
END
GO
